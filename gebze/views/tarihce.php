          Yöresel<br><br>

          Düğün: Düğünlerde köylerde dışarıda ateş yakılıp, kazanlarda düğün yemekleri pişirilir. Düğünlere gelenlere,
          düğün çorbası, etli yemek, pilav, zerde tatlısı hazırlanır. İki gün boyumca misafirlere bu yemekler ikram
          edilir. Düğünden birkaç gün önce kız evinde ya da damadın evinde çeyiz sergisi yapılır. Çeyiz sermeye
          gidenler, kendi aralarında küçük bir eğlence tertip ederler. Düğünler Cuma gününden itibaren başlar. Cuma
          günü gelin hamamı yapılır. Hamama köyün genç kızları da çağrılır. Genç kızlar gelin hamamına "kırmızı
          kurdelenin üzerine takman gelin teli" ile çağrılır. Hamamda gelin yıkanır ve hamama erkek evinden yemekler
          getirilir. Gelin hamamdan çalgılarla çıkartılır. Gelin kimselere görünmeden şemsiye altında hamamdan çıkar
          ve genç kızlarla evine gider. <br><br>

          Köylüyü düğüne çağırmak için, genç kızlar, içinde "kağıt şeker" olan torbalarla ev ev dolaşır. Kime köylerde
          ise genç kızlar "helva yemeğe buyurun" diyerek ekmek üzerine konmuş helvayla köylüyü kınaya çağırırlar. Bir
          başka köyde ise dilim ekmek üzerine baklava koyularak köy ahalisi düğüne davet edilir.<br><br>

          Büyük kına (düğün günü) gelin salona girerken genç erkekler dışarı çıkar. Genç kızlar düğünde şalvar,
          yeldirme ve bürme giyerler. Genç kızlar düğünde türkü ve maniler söylerler. Kınada gelinin başında ekmek
          kırılır, bir parçası geline yedirilir ve geri kalanı isteyene verilir. Ekmek, "bereket" diye
          kırılır.<br><br>

          Genç kızlara nişanlılık süreleri boyunca kaynanaları tarafından köyde yapılan her düğünde takı
          takar.<br><br>

          Cenaze: cenaze hoca tarafından soyulur, çenesi bağlanır ve üzerine çarşaf örtülür. Erkek cenazesi için
          çarşafın üstüne bıçak ve sabun; kadın cenazesinin üstüne ise makas ve sabun konulur. Cenaze, baş tarafı
          kıbleye gelecek şekilde yatırılır. Sabunla makas ölünün karnına y ada göğsünün üstüne koyulur. Amaç, ölünün
          karnının şişmesini önlemektir. Daha sonra ise bu sabunla ölü ve çamaşırları yıkanır. Ölü kadın ise, yüzüne
          havlu örtülür. Kişinin ölümü "sela" ile halka duyurulur. Cenazeyi bir kişi yıkar, üç kişi de su koyar. Mevta
          dışarıda yıkanmışsa orada 7 gece boyunca "karanlıkta kalmasın" diye ışık yakılır.<br><br>

          Bayramlar: Camilerde bayramın nerelerde kutlanacağı kararlaştırılır. Örneğin; birinci gün Duraklı'nın,
          ikinci gün Eleşli'nin bayramı olarak kararlaştırılır. Bayramlaşma camide başlar. Duraklı'nın bayramı olduğu
          gün, Duraklılara diğer köylerden ziyarete gelinir. Erkekler dışarıda olur; kadınlar ise evlere girer. O gün
          için pilav ve yemek yapılır. Camiden pilavların pişirilmesi için duyuru yapılır. Bir saat önceden herkes
          hazırlığını yapar. Bayram süresince kızlar ve erkekler gruplar halinde gezer.<br><br>

          Hıdırellez: Hıdırellez 5-6 Mayıs tarihlerinde yapılır. Hıdırellez'den üç gün önce mayasız hamur yoğurulur.
          Hamur taşarsa "Hıdırellezi "in geçtiği belli olur ve o hamur da bereketli olur. Hıdırellez geçtiğinde sütler
          dibine oturur. Hıdırellez sabahı, yağ tenekesi taşarsa, Hızır uğramış denir. O yağ alınıp boşaltılır ve
          kimseye söylenmezse evde bolluk olur. "Yağ taşmış, her yer batmış" denirse bolluk olmaz.<br><br>

          Akşamdan genç kızlar, küplere yüzük, bilezik gibi eşyalarını koyar ve küpü gül diplerine gömerler. Erkekler
          gül diplerinde küp ararlar ve bulurlarsa kızlardan bahşiş almadan küpü vermezler.<br><br>

          Hızır Aleyhisselam'ın gül diplerinde gezindiğine inanılır. Küpü evde saklayan kız evde kalır. Gençler ateş
          yakar ve üstünden atlar. Pasalle dumanından atlayanı gumuşların (böcekle) yemeyeceğine inanılır.
          Hıdırellez'de dilek tutulur. Soğan yaprakları kesilerek renkli ip bağlanır. Birine" cefa" birine "sefa"
          denir. Sefa yaprağı uzadı ise dilek kabul olur. Cefa yaprağı uzarsa dilekğin kabul olmadığına
          inanılır.<br><br>

          Yöresel Yemekler: Çarşır Mancarı, Kazayağı Mancarı, Ebegümeci Mancarı, Efelik Mancarı, Mantı, Yamayuka
          Böreği, Tava Tutuşturması, Bulgurlu Börek, Sirkem Mancar, Kabak Tatlısı, Höşmerlim, Peynir Höşmeli,
          Kocagörmez, Cızlama(Nazlım), Kabaklı Börek, Tartı(tarta-dartı) <br><br>

          Giysiler: Gebze'nin dağ köylerinde yaşayan ve özellikle keten ekerek geçinen bölümlerde halk, kendi el
          örmesi elbiseleri giyer. Kadınlar çoğunlukla şalvar, yelek ve hırka giyer. Başlarına işlemeli yazma ya da
          beyaz yazmalar takarlar. Boyunlarına gerdanlık takarlar. Elbiseleri çoğunlukla basmadan yapılmıştır.
