        <h3>Tarihçe</h3>
        <h5>Tarihte Gebze</h5>
        <p>Gebze'nin de içinde bulunduğu, eski Yunanlılar'ın ve Romalılar'ın Bitinya (Bithynie) dedikleri coğrafi
          bölgenin bilinen en eski tarihi, M.Ö. XII yüzyıla kadar dayanır. Bölge, özellikle Kocaeli Yarımadası,
          coğrafi konumunun öneminden dolayı, tarihin hemen hemen bütün dönemlerinde, birçok ulusa yurt olmuştur. Asya
          ile Avrupa kıtaları arasındaki en önemli geçit yeri olan Kocaeli Yarımadası ya bir çok ulusun yurdu, ya da
          gelip geçtikleri, medeniyetlerinden izler bıraktığı bir yer olmuştur.<br><br>

          Bilinen ilk ulus göçü de M.Ö. XII. yüzyılın başlarındadır. Bu ulus Yunan kökenli Frikler'dir. Boğaz
          (Bosforos) yoluyla Anadolu'ya inmişlerdir. XII yüzyıla kadar Trakya'dan İzmit dolaylarına göçler devam
          etmiştir. Fakat bu dönemde eski Gebze'nin yerine dair hiçbir bilgi edinilememiştir. Kısaca antik çağ
          Gebze'sinin yeri kesinlikle bilinememektedir.<br><br>

          Bugün Gebze'nin olduğu yerde, M.Ö. 281-246 yıllarında Kral 1. Nicomede'nin egemenliğindeki Bitinya Krallığı
          döneminde Dakibyza ve Libyssa adında yerleşmeler vardır. Eski Gebze'nin yerine dair söylenenler, işte bu
          tarihlere aittir. Daha eski tarihlere ait bilgiler ise çelişkilidir.<br><br>

          Bu yerleşim alanlarının araştırmalara konu olmasının en önemli nedeni ise, ünlü Kartacalı komutan
          Hannibal'ın krallık döneminde burada yerleşmiş olmasıdır.<br><br>

          Hannibal Zama harbindeki yenilgisinden sonra ülkesinde itibar görmemiş ve Bitinya Krallığı'na iltica etmek
          zorunda kalmıştır. Bitinya Kralları I. ve II. Prusias'ın savaş danışmanlıklarını yapmıştır. II. Prusias'ın
          ihaneti sonucu düşmanın eline düşmemek için intihar etmiş ve Lybissa'ya gömülmüştür.<br><br>

          İşte birçok tarihçinin ve araştırmacının eski Gebze olduğu iddia edilen bu yeri araştırmasının en büyük
          nedeni budur. Hannibal'ın burayı seçmesinin birçok nedeni vardır. Devamlı izlenme kuşkusu, Nicomedia başkent
          olduğu için gelenin gidenin çok olması ve tanınma ihtimalinin fazla olması, yönetime güvenmemesi bu
          nedenlerin başlıcalarıdır.<br><br>

          Roma kuvvetlerinden gizlenen Hannibal, korunaklı, kaçışı kolay ve denizle ilişkili bir yer aramıştır.
          Sonunda bu özelliklere sahip Libyssa'yı seçmiştir.<br><br>

          O dönemde Libyssa'nın kurulduğu yer, hem denize hem de karaya hakim bir tepe üzerindedir. Tepe, körfezin en
          dar yeridir.<br><br>

          1330 yılında Osmanlılarla Bizans arasında yapılan savaştan sonra Gebze'nin de içinde bulunduğu bölge,
          Osmanlı idaresine dahil edilmiştir.<br><br>

          Bugünkü Gebze'nin kurucusu Orhan Gazi'dir. Gebze'de kendi adına cami de yaptıran Orhan Gazi, bölgede izler
          bırakan ilk Türk büyüğüdür. Orhan Gazi, bölgenin imarı ve yaşaması için büyük çabalar göstermiştir. Bu
          amaçla işletmeler kurmuş, vakıfları desteklemiştir<br><br>

          Osmanlıların devlet olma çabaları sırasında, Gebze yine ordugah yerleşimi olarak kullanılmıştır. Osmanlı
          Beyliğinin kurulmasında büyük emekleri geçen Akçakoca Bey'in oğlu olan İlyas Çelebi de hem Gebze'nin
          fethinde hem de kuruluşunda büyük rol oynamıştır.<br><br>

          Gebze Osmanlı İmparatorluğunun son yıllarına kadar kimi zaman İstanbul'a, daha çok da Kocaeli'ye bağlı bir
          kaza olarak, önemli bir yer niteliğini uzun yıllar korumuştur. 1. Dünya Savaşı'nda Osmanlı İmparatorluğunun
          yenik düşmesi üzerine Anadolu ve Trakya'nın birçok yöresi gibi Gebze'de düşman kuvvetleri tarafından işgal
          edilmiştir.

          1920 yılznda İngilizler'in bölgeyi işgaline, 1921 yılının başlarında Yunanlılar da katılmıştır. Daha sonra
          Anadolu içerisinde yenilgiye uğrayan Yunan kuvvetleri, amaçlarına ulaşamamanın üzüntüsüyle geldikleri yoldan
          geriye kaçmışlardır.<br><br>

          Bu yıllarda Gebze, Anadolu'nun en dikkate değer yerlerinden biridir. Türk kuvvetlerinin biraz ilerisinde
          İngiliz askerleri bulunmaktaydı.<br><br>

          18-19 Ocak 1923 tarihli Hakimiyet-i Milliye-Ankara Gazetesi'nde Atatürk'ün bölgeyi ve Gebze'yi ziyaret
          ettiğinden bahsedilir. Atatürk Gebze'deki askeri birliklerin durumundan memnun kalarak geri dönmüştür.
          İstanbul'un terk edilmesinden sonra Gebze ve Çevresi tamamen emniyet altına alınmıştır.<br><br>

          Cumhuriyet'in ilanına kadar kimi zaman İstanbul, kimi zaman da Kocaeli'ye bağlı bir kaza olan Gebze,
          Cumhuriyet'in ilanından sonra yeni iller kanununa göre il olan İzmit'e bağlanmıştır.<br><br>

          Libyssa'dan Gebze'ye<br><br>

          Gebze adı köken olarak, diğer eski yerleşmelerin ismine bağlanmaktadır. Araştırmacıların bir çoğu bu
          görüştedir. Bazılarıysa Libyssa ve Dakibyza isimlerini bazı ufak değişikliklerle kullanmışlardır.<br><br>

          Antik çağ araştırmacılarının hemen hemen hepsi Libyssa adını kullanmışlardır. Roma ve Bizans döneminde
          Dakibyza adı da kullanılmaya başlanan bir diğer isimdir. Okunuş açısından da bu isimlerin Gebze sözcüğünü
          andırması, kelimenin kökeninin çok eski olduğunu kanıtlamaktadır.<br><br>

          Bazı araştırmacılar da yöreden bahsederken, Gebseh, Gebisseh, Gjabseh isimlerini kullanmışlardır. Gekbuze,
          Ghviza, Gavize, Dschebse, Dschebize, Gebize de kullanılan diğer isimlerden bazılarıdır.<br><br>

          Evliya Çelebi'nin Seyahatnamesi'nde de bir kez Kekbeziye ismini kullanmış, Erzurum Seyahatı esnasındaysa
          Gebze kelimesinin Gelbize'den kaynaklandığını yazmıştır.<br><br>

          İbrahim Hakkı Konyalı ise, eski Osmanlı arşiv kaynaklarında Geybüyze, Geybüveyze, Geyibüveyze, Geyiboyze,
          Geykivize şeklinde yazıldığını, halen yaşayan ismininse Gebze olduğunu vurgulamıştır.<br><br>

          Bazı araştırmacılar da, "Gebze"nin bir zamanlar Osmanlı ve Bizans savaşçıları arasında sık sık el değiştiren
          ve özlenen bir yöre olması itibariyle "Gel bize" veya "Bize gel" ifadelerinden oluşan ve zaman içinde
          değişerek, halkın öz dilinde "Gebze"ye dönüşen bir ad olduğunu belirtmişlerdir. Ancak, 1640 yılında
          "Gebze"ye geldiği anlaşılan Evliye Çelebi, ünlü Seyahatname'sinin ilgili bölümünde (Gebze, "Gelbize"den
          galattır.) ifadesiyle bu konuyu vurgulamaktadır.<br><br>

